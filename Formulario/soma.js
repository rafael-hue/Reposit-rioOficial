function Somar() {
    var num1 = document.getElementById('num1').value;
    var num2 = document.getElementById('num2').value;
    var soma = parseInt(num1)+parseInt(num2);
    document.write(soma);
   }
   function Dividir() {
    var num1 = document.getElementById('num1').value;
    var num2 = document.getElementById('num2').value;
    var soma = parseInt(num1)/parseInt(num2);
    document.write(soma);
   }
   function Subtrair() {
    var num1 = document.getElementById('num1').value;
    var num2 = document.getElementById('num2').value;
    var soma = parseInt(num1)-parseInt(num2);
    document.write(soma);
   }
   function Multiplicar() {
    var num1 = document.getElementById('num1').value;
    var num2 = document.getElementById('num2').value;
    var soma = parseInt(num1)*parseInt(num2);
    document.write(soma);
   }


  
